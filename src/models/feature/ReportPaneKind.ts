/**
 * Enum representing a ReportPaneKind.
 */
export enum ReportPaneKind {
  /** Represents no pane. */
  None = 'none',

  /** Represents the left pane. */
  LeftPane = 'leftPane',

  /** Represents the center pane. */
  CenterPane = 'centerPane',

  /** Represents the right pane. */
  RightPane = 'rightPane'
}
