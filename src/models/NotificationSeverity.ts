/**
 * Enum representing the severity levels of a notification.
 */
export enum NotificationSeverity {
  /**
   * Informational message.
   */
  Info = 'info',

  /**
   * Success message.
   */
  Success = 'success',

  /**
   * Warning message.
   */
  Warning = 'warn',

  /**
   * Error message.
   */
  Error = 'error',
}
