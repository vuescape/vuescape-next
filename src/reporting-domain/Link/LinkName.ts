/**
 * Class representing predefined link names.
 */
export class LinkName {
  /** Represents a self link. */
  public static readonly Self = '__vs-self'

  /** Represents a link for downloading CSV. */
  public static readonly DownloadCsv = '__vs-downloadCsv'

  /** Represents a link for downloading PDF. */
  public static readonly DownloadPdf = '__vs-downloadPdf'
}
