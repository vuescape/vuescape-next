/**
 * Enum representing a UnitOfMeasure.
 * These are HTML units of measure.
 */
export enum UnitOfMeasure {
  /** Represents no unit of measure. */
  None = 0,

  /** Represents a percentage unit of measure. */
  Percent,

  /** Represents a pixel unit of measure. */
  Pixel,

  /** Represents an em unit of measure. */
  Em,
}
